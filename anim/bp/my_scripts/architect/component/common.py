from ..core.basic import isServer, compClient, compServer

def _nativeCompGet(entityId, name):
    # type: (str, str) -> object
    if isServer():
        return getattr(compServer, 'Create' + name[1:])(entityId)
    else:
        return getattr(compClient, 'Create' + name[1:])(entityId)
    

